void setup() {
  Serial.begin(115200);
  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
    Serial.println(F("OLED 初始化失败"));
    while (true);
  }
  display.begin(SSD1306_SWITCHCAPVCC, 0x3C, true, false);
  display.clearDisplay();
  display.setTextSize(1);
  display.setTextColor(35,5);
  display.drawBitmap(10, 0, image_data, 64, 62, WHITE);
  display.display();
}

